package Conceptos.errores;

public class TiposDeError {

    /*
    1. Error en tiempo de ejecución

        Cuando ocurre un error en el programa para el que no estamos preparados y hace que el programa aborte.
        Ej.: hacer la división X/0

    2. Error de compilación

        Me falta un caracter o dato que necesita el lenguaje para compilar el programa.
        No se está cumpliendo con la sintaxis del lenguaje por lo que el compliador no entiende la instrucción.


    3. Error lógico/error humano

        Error del programador.
        Ej.:
        int temperatura = 25;
        if (temperatura >= 25){
            System.out.println("Hace frio!");
            }

        El error es nuestro por poner que a más de 25 grados hace frio.
        El problema de este tipo de errores es que no nos va a salir un aviso y son muy difíciles de detectar.

     */





}
