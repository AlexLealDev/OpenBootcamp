package Conceptos.estructurasDeControl;

public class ForLoop {

    public static void main(String[] agrs) {

        for (int i = 0; i < 10; i++) {
            System.out.println("Estoy en una condición for");

        // Se suele utilizar como variable i, j, k, l por ese orden
        // dentro de los paréntesis del for hay 3 condiciones
        /*
        1. int i = 0 -> Es la condición de comienzo
        2. i < 10 -> Es la condición de parada
        3. i++ -> Es la condición de paso
         */
        }
    }
}
